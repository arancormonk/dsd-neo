float
dsd_input_filter(float sample, int mode) {
    float sum;
    int i;
    float gain;
    int zeros;
    float *v, *coeffs;
    int* headp = NULL;
    switch (mode) {
        case 1:
            gain = ngain;
            v = xv;
            coeffs = xcoeffs;
            zeros = NZEROS;
            headp = &xv_head;
            break;
        case 2:
            gain = nxgain;
            v = nxv;
            coeffs = (float*)nxcoeffs;
            zeros = NXZEROS;
            headp = &nxv_head;
            break;
        case 3:
            gain = dmrgain;
            v = dxv;
            coeffs = (float*)dmrcoeffs;
            zeros = DMRNZEROS;
            headp = &dxv_head;
            break;
        case 4:
            gain = dpmrgain;
            v = dpmrxv;
            coeffs = (float*)dpmrcoeffs;
            zeros = DPMRNZEROS;
            headp = &dpmrxv_head;
            break;

        case 5:
            gain = m17gain;
            v = m17xv;
            coeffs = (float*)m17coeffs;
            zeros = M17ZEROS;
            headp = &m17xv_head;
            break;

        case 6:
            gain = p25gain;
            v = p25xv;
            coeffs = (float*)p25coeffs;
            zeros = P25ZEROS;
            headp = &p25xv_head;
            break;

        default: return sample;
    }

    /* Circular buffer update: write newest sample at head and convolve
       in the same logical order as the legacy linear buffer (oldest->newest). */
    const int cap = zeros + 1;
    int head = *headp;
    head++;
    if (head >= cap) {
        head = 0;
    }
    v[head] = (float)sample; /* most recent */
    *headp = head;

    sum = 0.0f;
    for (i = 0; i <= zeros; i++) {
        int idx = head - (zeros - i);
        if (idx < 0) {
            idx += cap;
        }
        sum += coeffs[i] * v[idx];
    }

    return sum / gain; // filtered sample out
}

